function GetUserDefinedComponentOptions(componentName: FormComponentsType) {
	return get(opts).theComponentImports.find((x) => x.componentName == componentName);
}

export function GetComponentSelectionMetaData(
	componentName: FormComponentsType
): ComponentSelectionMetaData {
	const userDefinedOptions = GetUserDefinedComponentOptions(componentName);

	const result: ComponentSelectionMetaData = {};
	result.categories = userDefinedOptions?.categories ?? defaultCategories[componentName];
	result.keywords = userDefinedOptions?.keywords ?? defaultKeywords[componentName];
	result.icon = userDefinedOptions?.icon ?? defaultIcons[componentName];

	return result;
}

